export const ProjectsGallery = () => {
  const [activeCategory, setActiveCategory] = useState("All");
  const [selectedProject, setSelectedProject] = useState<Project | null>(null);
  const categories = ["All", "VRChat Worlds", "Content Creation", "Community", "Free Tools"];

  const filteredProjects = activeCategory === "All" 
    ? projects 
    : projects.filter(project => project.category === activeCategory);

  const handleProjectSelect = (project: Project) => {
    if (project.videoId) {
      setSelectedProject(project);
    } else {
      window.open(project.link, '_blank');
    }
  };

  return (
    <section className="py-12 bg-black" id="our-projects">
      <div className="container mx-auto px-4">
        <motion.h2
          initial={{ opacity: 0 }}
          whileInView={{ opacity: 1 }}
          viewport={{ once: true }}
          className="text-7xl font-bold text-center mb-8 py-4 bg-gradient-to-r from-blue-400 via-purple-500 to-pink-400 text-transparent bg-clip-text leading-relaxed"
        >
          Our Projects
        </motion.h2>

        <CategoryFilter
          categories={categories}
          activeCategory={activeCategory}
          onSelectCategory={setActiveCategory}
        />
        
        <ProjectList
          projects={filteredProjects}
          onSelect={handleProjectSelect}
        />
      </div>

      <ProjectDialog
        project={selectedProject}
        onClose={() => setSelectedProject(null)}
      />
    </section>
  );
};
